            <Abstract>
                <AbstractText Label="PURPOSE" NlmCategory="OBJECTIVE">Survivors of childhood cancer are at an increased risk of developing subsequent neoplasms. In long-term survivors of childhood malignancies treated with and without cranial radiation therapy (CRT), undergoing unenhanced magnetic resonance imaging (MRI) of the brain, we estimated detection of intracranial neoplasms.</AbstractText>
                <AbstractText Label="METHODS" NlmCategory="METHODS">To investigate neurocognitive outcomes, 219 survivors of childhood cancer underwent unenhanced screening MRI of the brain. Of the survivors, 164 had been treated for acute lymphoblastic leukemia (ALL) (125 received CRT) and 55 for Hodgkin lymphoma (HL) (none received CRT). MRI examinations were reviewed and systematically coded by a single neuroradiologist. Demographic and treatment characteristics were compared for survivors with and without subsequent neoplasms.</AbstractText>
                <AbstractText Label="RESULTS" NlmCategory="RESULTS">Nineteen of the 219 survivors (8.7 %) had a total of 31 subsequent intracranial neoplasms identified by neuroimaging at a median time of 25 years (range 12-46 years) from diagnosis. All neoplasms occurred after CRT, except for a single vestibular schwannoma within the cervical radiation field in a HL survivor. The prevalence of subsequent neoplasms after CRT exposure was 14.4 % (18 of 125). By noncontrast MRI, intracranial neoplasms were most suggestive of meningiomas. Most patients presented with no specific, localizing neurological complaints. In addition to the schwannoma, six tumors were resected based on results of MRI screening, all of which were meningiomas on histologic review.</AbstractText>
                <AbstractText Label="CONCLUSION" NlmCategory="CONCLUSIONS">Unenhanced brain MRI of long-term survivors of childhood cancer detected a substantial number of intracranial neoplasms. Screening for early detection of intracranial neoplasms among aging survivors of childhood cancer who received CRT should be evaluated.</AbstractText>
                <AbstractText Label="IMPLICATIONS FOR CANCER SURVIVORS" NlmCategory="CONCLUSIONS">The high prevalence of incidentally detected subsequent intracranial neoplasms after CRT in long-term survivors of childhood cancer and the minimal symptoms reported by those with intracranial tumors in our study indicate that brain MRI screening of long-term survivors who received CRT may be warranted. Prospective studies of such screening are needed.</AbstractText>
            </Abstract>
